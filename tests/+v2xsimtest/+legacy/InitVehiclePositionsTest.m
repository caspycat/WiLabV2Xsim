            appParams = struct( ...
                nRSUs=2, ...
                RSU_xLocation=[100,200], ...
                RSU_yLocation=[20,30]);
            [~,simValues,positionManagement] = ...
                testCase.initializeScenario( ...
                    "BrownianMotionScenario",{ ...
                    "scenarioOptions.VehicleCount",10, ...
                    "positionErrorModules","Gaussian", ...
                    "positionErrorOptions.Gaussian." + ...
                        "StandardDeviationMeters",10}, ...
                    appParams);

            world = simValues.world;
            vehicleIDs = testCase.ueIndices(world,world.VehicleIds);
            rsuIDs = testCase.ueIndices(world,world.RsuIds);
            actualPositions = [ ...
                positionManagement.XvehicleReal, ...
