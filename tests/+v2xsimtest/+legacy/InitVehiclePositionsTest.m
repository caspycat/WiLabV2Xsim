        function testConfiguredFalseExitFollowsRampTrajectory(testCase)
            [simParams,simValues,positionManagement] = ...
                testCase.initializeScenario( ...
                    "ExitRampHighwayScenario",{ ...
                    "simulationTime",10, ...
                    "positionTimeResolution",0.1, ...
                    "scenarioOptions.VehicleCount",500, ...
                    "scenarioOptions.RoadLength",200, ...
                    "scenarioOptions.MeanVehicleSpeed",20, ...
                    "scenarioOptions.VehicleSpeedStandardDeviation",0, ...
                    "scenarioOptions.RerollSpeedOnWrapAround",false, ...
                    "scenarioOptions.ExitProbability",0, ...
                    "scenarioOptions.MergeDistance",40, ...
                    "positionErrorModules","FalseExit", ...
                    "positionErrorOptions.FalseExit." + ...
                        "CurseProbability",1});
            routeStates = ...
                simValues.world.TrafficScenario.VehicleRouteStates;
            vehicleIndex = find(routeStates.Route == "Outer",1);
            testCase.assertNotEmpty(vehicleIndex);
            vehicleName = routeStates.Properties.RowNames{vehicleIndex};
            vehicleID = testCase.ueIndices( ...
                simValues.world,string(vehicleName));

            currentTime = 0;
            while simValues.world.TrafficScenario. ...
                    VehicleRouteStates.Route( ...
                    vehicleName) == "Outer"
                currentTime = currentTime + ...
                    simParams.positionTimeResolution;
                [positionManagement,simValues] = ...
                    testCase.advanceObservedPositions( ...
                        simParams,simValues, ...
                        positionManagement,currentTime);
                testCase.assertLessThan(currentTime,6);
            end

            sampleCount = 10;
            actualTrajectory = zeros(sampleCount,2);
            apparentTrajectory = zeros(sampleCount,2);
            expectedRampTrajectory = zeros(sampleCount,2);
            rampProgress = zeros(sampleCount,1);
            for sampleIndex = 1:sampleCount
                scenario = simValues.world.TrafficScenario;
                routeState = ...
                    scenario.VehicleRouteStates(vehicleName,:);
                testCase.verifyTrue(ismember( ...
                    routeState.Route,["Merge","Adjacent"]));

                actualPosition = ...
                    scenario.VehicleKinematics(vehicleName,["X","Y"]);
                actualTrajectory(sampleIndex,:) = [ ...
                    actualPosition.X,actualPosition.Y];
                geometry = scenario.Geometry;
                distancePastFork = routeState.TravelDirection .* ...
                    (actualPosition.X - geometry.ForkX);
                rampProgress(sampleIndex) = ...
                    distancePastFork ./ (geometry.RoadLength ./ 2);
                expectedRampTrajectory(sampleIndex,:) = ...
                    scenario.LaneNetwork.evaluate( ...
                        geometry.getRampLaneId( ...
                            routeState.TravelDirection), ...
                        rampProgress(sampleIndex));
                apparentTrajectory(sampleIndex,:) = [ ...
                    simValues.XvehicleEstimated(vehicleID), ...
                    simValues.YvehicleEstimated(vehicleID)];

                currentTime = currentTime + ...
                    simParams.positionTimeResolution;
                [positionManagement,simValues] = ...
                    testCase.advanceObservedPositions( ...
                        simParams,simValues, ...
                        positionManagement,currentTime);
            end

            testCase.verifyEqual( ...
                apparentTrajectory,expectedRampTrajectory, ...
                AbsTol=1e-12);
            testCase.verifyNotEqual( ...
                apparentTrajectory,actualTrajectory);
            testCase.verifyGreaterThan(diff(rampProgress),0);
        end

        function testConfiguredFalseMergeFollowsMainlineTrajectory( ...
                testCase)
            [simParams,simValues,positionManagement] = ...
                testCase.initializeScenario( ...
                    "ExitRampHighwayScenario",{ ...
                    "simulationTime",10, ...
                    "positionTimeResolution",0.1, ...
                    "scenarioOptions.VehicleCount",500, ...
                    "scenarioOptions.RoadLength",200, ...
                    "scenarioOptions.MeanVehicleSpeed",20, ...
                    "scenarioOptions.VehicleSpeedStandardDeviation",0, ...
                    "scenarioOptions.RerollSpeedOnWrapAround",false, ...
                    "scenarioOptions.ExitProbability",1, ...
                    "scenarioOptions.MergeDistance",20, ...
                    "positionErrorModules","FalseMerge", ...
                    "positionErrorOptions.FalseMerge." + ...
                        "CurseProbability",1});
            routeStates = ...
                simValues.world.TrafficScenario.VehicleRouteStates;
            vehicleIndex = find(routeStates.Route == "Outer",1);
            testCase.assertNotEmpty(vehicleIndex);
            vehicleName = routeStates.Properties.RowNames{vehicleIndex};
            vehicleID = testCase.ueIndices( ...
                simValues.world,string(vehicleName));

            currentTime = 0;
            while simValues.world.TrafficScenario. ...
                    VehicleRouteStates.Route( ...
                    vehicleName) == "Outer"
                currentTime = currentTime + ...
                    simParams.positionTimeResolution;
                [positionManagement,simValues] = ...
                    testCase.advanceObservedPositions( ...
                        simParams,simValues, ...
                        positionManagement,currentTime);
                testCase.assertLessThan(currentTime,6);
            end

            sampleCount = 20;
            actualTrajectory = zeros(sampleCount,2);
            apparentTrajectory = zeros(sampleCount,2);
            expectedMainlineTrajectory = zeros(sampleCount,2);
            distancePastFork = zeros(sampleCount,1);
            onMergeConnector = false(sampleCount,1);
            for sampleIndex = 1:sampleCount
                scenario = simValues.world.TrafficScenario;
                routeState = ...
                    scenario.VehicleRouteStates(vehicleName,:);
                testCase.verifyEqual(routeState.Route,"Ramp");

                actualPosition = ...
                    scenario.VehicleKinematics(vehicleName,["X","Y"]);
                actualTrajectory(sampleIndex,:) = [ ...
                    actualPosition.X,actualPosition.Y];
                geometry = scenario.Geometry;
                distancePastFork(sampleIndex) = ...
                    routeState.TravelDirection .* ...
                    (actualPosition.X - geometry.ForkX);
                onMergeConnector(sampleIndex) = ...
                    distancePastFork(sampleIndex) <= ...
                    geometry.MergeDistance;

                if onMergeConnector(sampleIndex)
                    expectedMainlineTrajectory(sampleIndex,:) = ...
                        scenario.LaneNetwork.evaluate( ...
                            geometry.getMergeLaneId( ...
                                routeState.TravelDirection), ...
                            distancePastFork(sampleIndex) ./ ...
                                geometry.MergeDistance);
                else
                    laneID = geometry.getMainlineLaneId( ...
                        routeState.TravelDirection, ...
                        geometry.NLanes - 1);
                    progress = actualPosition.X ./ ...
                        geometry.RoadLength;
                    if routeState.TravelDirection < 0
                        progress = 1 - progress;
                    end
                    expectedMainlineTrajectory(sampleIndex,:) = ...
                        scenario.LaneNetwork.evaluate( ...
                            laneID,progress);
                end
                apparentTrajectory(sampleIndex,:) = [ ...
                    simValues.XvehicleEstimated(vehicleID), ...
                    simValues.YvehicleEstimated(vehicleID)];

                currentTime = currentTime + ...
                    simParams.positionTimeResolution;
                [positionManagement,simValues] = ...
                    testCase.advanceObservedPositions( ...
                        simParams,simValues, ...
                        positionManagement,currentTime);
            end

            testCase.verifyTrue(any(onMergeConnector));
            testCase.verifyTrue(any(~onMergeConnector));
            testCase.verifyEqual( ...
                apparentTrajectory,expectedMainlineTrajectory, ...
                AbsTol=1e-12);
            testCase.verifyNotEqual( ...
                apparentTrajectory,actualTrajectory);
            testCase.verifyGreaterThan(diff(distancePastFork),0);
        end
    end

    methods (Access = private)
        function [simParams,simValues,positionManagement,appParams] = ...
                initializeScenario( ...
                testCase,typeOfScenario,scenarioArguments,appParams)
            if nargin<4
                appParams = struct(nRSUs=0);
            end
            [simParams,remainingArguments] = ...
                testCase.createSimulationParameters( ...
                    typeOfScenario,scenarioArguments);
            testCase.assertEmpty(remainingArguments{1});
            simParams.technology = constants.TECH_ONLY_11P;

            [simParams,simValues,positionManagement,appParams] = ...
                initVehiclePositions(simParams,appParams);
        end

        function [simParams,remainingArguments] = ...
                createSimulationParameters( ...
                ~,typeOfScenario,scenarioArguments)
            inputArguments = [ ...
                {"seed",1,"typeOfScenario",typeOfScenario}, ...
                scenarioArguments];
            [simParams,remainingArguments] = ...
                initiateMainSimulationParameters("",inputArguments);
        end

        function [positionManagement,simValues] = ...
                advanceObservedPositions( ...
                ~,simParams,simValues,positionManagement,currentTime)
            [~,~,~,~,positionManagement,simValues] = updatePosition( ...
                currentTime,simValues.IDvehicle, ...
                simParams.positionTimeResolution, ...
                positionManagement,simValues, ...
                struct(printSpeed=false),simParams);

            simValues.XvehicleEstimatedLegacy = ...
                positionManagement.XvehicleReal;
            simValues.YvehicleEstimatedLegacy = ...
                positionManagement.YvehicleReal;
            simValues.XvehicleEstimated = ...
                simValues.XvehicleEstimatedLegacy;
            simValues.YvehicleEstimated = ...
                simValues.YvehicleEstimatedLegacy;
            simValues = applyPositionErrorChain( ...
                simParams,simValues,currentTime);
        end

        function verifyProjection(testCase,simValues,positionManagement)
            world = simValues.world;
            testCase.verifyWorldIdentity(world);
            vehicleKinematics = world.TrafficScenario. ...
                VehicleKinematics(cellstr(world.VehicleIds),:);
            vehicleIDs = testCase.ueIndices(world,world.VehicleIds);
            rsuIDs = testCase.ueIndices(world,world.RsuIds);
            uePositions = world.UePositions;

            testCase.verifyClass(positionManagement.XvehicleReal,"double");
            testCase.verifySize( ...
                positionManagement.XvehicleReal, ...
                [numel(world.UeIds),1]);
            testCase.verifyEqual( ...
                positionManagement.XvehicleReal,uePositions.X);
            testCase.verifyEqual( ...
                positionManagement.YvehicleReal,uePositions.Y);

            expectedSpeed = hypot(vehicleKinematics.vX,vehicleKinematics.vY);
            expectedDirection = complex(zeros(height(vehicleKinematics),1));
            movingVehicles = expectedSpeed>0;
            expectedDirection(movingVehicles) = complex( ...
                vehicleKinematics.vX(movingVehicles)./ ...
                    expectedSpeed(movingVehicles), ...
                vehicleKinematics.vY(movingVehicles)./ ...
                    expectedSpeed(movingVehicles));
            testCase.verifyEqual( ...
                positionManagement.v(vehicleIDs),expectedSpeed);
            testCase.verifyEqual( ...
                positionManagement.direction(vehicleIDs), ...
                expectedDirection,AbsTol=1e-14);
            testCase.verifyEqual( ...
                positionManagement.v(rsuIDs),zeros(numel(rsuIDs),1));
            testCase.verifyEqual( ...
                positionManagement.direction(rsuIDs), ...
                zeros(numel(rsuIDs),1));
            testCase.verifyEqual( ...
                positionManagement.XVehicle, ...
                positionManagement.XvehicleReal);
            testCase.verifyEqual( ...
                positionManagement.YVehicle, ...
                positionManagement.YvehicleReal);
        end

        function verifyWorldIdentity(testCase,world)
            testCase.verifyEqual( ...
                world.UeIds,[world.VehicleIds;world.RsuIds]);
            testCase.verifyEmpty(intersect(world.VehicleIds,world.RsuIds));
            testCase.verifyEqual( ...
                numel(unique(world.UeIds)),numel(world.UeIds));
        end

        function indices = ueIndices(testCase,world,ueIds)
            [isPresent,indices] = ismember(string(ueIds),world.UeIds);
            testCase.assertTrue(all(isPresent));
            indices = indices(:);
        end
